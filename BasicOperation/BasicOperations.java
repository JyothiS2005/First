public class BasicOperations{
    public static void main(String[] args){
        int a = 15;
        int b = 5;
        int Sum = a + b;
        int diff = a - b;
        int prod = a * b;
        System.out.println("Addition: " + Sum);
        System.out.println("Subtraction: " + diff);
        System.out.println("Multiplication: " + prod);



    }
}
